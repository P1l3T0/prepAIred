/// <summary>
/// Represents the type of a question in a survey or questionnaire.
/// </summary>
public enum QuestionType
{
    /// <summary>
    /// Represents a single selectable choice in a collection of options.
    /// </summary>
    SingleChoice,
    /// <summary>
    /// Represents a multiple-choice question or selection, typically used in quizzes, surveys, or forms.   
    /// </summary>
    MultipleChoice,
    /// <summary>
    /// Represents an open-ended operation or state that does not have predefined constraints or limits.
    /// </summary>
    OpenEnded
}